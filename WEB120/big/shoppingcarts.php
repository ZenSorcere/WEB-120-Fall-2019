<p>Setting up Shopping Carts used to be a daunting task of coding, security, and information tracking, like customer data and inventory management. These days, there are a plethora of options to choose from that do all the heavy lifting for you.  However, it’s still a daunting task, just choosing which one is right for you and your business. There are so many metrics to sort by, with determining what level of integration they have with your current site, or if they provide site building services, what level of customer support they offer, types of payments they can process, and of course, how much they cost. It’s enough to give anyone analysis paralysis.</p>

<p>The two main things you’ll need are a merchant account and a payment gateway. Many of the cart-in-a-box options include those in their services, or allow a user to use their own merchant account (like through Paypal, for example). Once you have those determined, or are fine with the built-in options available from the third part options, you want to focus on which options would work best for your site’s needs and what kind of selling volume or site traffic you are expecting.  If you are not an experienced coder, you will want to go with an easy to install vendor option, as not all of them are non-coder friendly.  You’ll want to keep an eye out for other potential pitfalls: many of the shopping cart vendors have bandwidth limitations (which might involve overage fees), product limits, transaction fees, and features that might be limited to higher pricing tiers.</p>

<p>Pricing-wise, most of the third party vendors do offer free trial periods (though actual selling may not be an option on a free trial tier), and they have pricing tiers that can vary on their monthly fees from $9.99-$24/month or more.</p>

<p>For a small home business or a hobbyist, there are a couple of free/open source options that might be worth looking into:</p>

<p><a href="https://Freewebstore.com" target="_blank" title="Freewebstore.com">Freewebstore.com</a></p>
<p>This site claims to offer you everything you need, for free.  You get a website, SSL, a domain, and a shopping cart, and support. Setting up an account requires no credit card. They promise a ton of features, including app integration, mobile, SEO optimization, analytics, and management and marketing features. Sounds too good to be true...but worth checking out!</p> 

<p><a href="https://Opencart.com" target="_blank" title="Opencart.com">Opencart.com</a></p>
<p>This shopping cart is an open source version, using PHP and MySQL. It is free to use under the GNU General Public License. They also claim to offer support, unlimited products, store management options, thousands of templates for customization, responsive by default, and customer tracking options. Another one that sounds like it would be worth investigating for any person starting up a business!</p>
    
 
    
