<p>When it comes to different gallery options for website use, there are almost as many options available these days as there are web pages. How do you determine which might be the best for your needs? There are a number of categories you could look into, purchase options, as well as self-coding or pre-packaged code. It really depends on what you are hoping to get out of it.</p>

<p>Galleries come in a variety of options: Slideshows are usually presented at the top of a web page to highlight important content.  Seeing them in use on the internet, these tend to auto-rotate through the slides, and include controls for slide navigation, should you wish to control the flow yourself. Actual Galleries tend to highlight image collections, without necessarily automated rotation through the photos, and are frequently used in blogs or photographic portfolio presentations.</p>


<h3>Which Gallery to Choose?</h3>

<p>When it comes to choosing between a free or a paid version, it really depends on what works best for what you are doing.  A personal site may not require anything robust, and there are plenty of free options to choose from in that regard.  If you want something more robust, with tons of features (and possibly support!), then a paid version might be a better option.  This can also be a smart choice if you want something dependable that doesnâ€™t require a lot of time on your part to get set up; if the code is already created and all you have to do is drop the photos and content in, a strong and stable pre-coded, paid gallery might serve your needs best.</p>

<p>Looking through the various gallery options that are out there, there are a number of collection pages that delved into the varied options.  These days, the most interesting and interactive galleries all seem to be built with Jquery, though there are still some out there that are straight CSS based.  Nearly all of these galleries boast multiple templates and skins, dozens of transition animations, easy tying-in with Wordpress and Joomla, and fully responsive options to work on all devices.</p>

<h3>Galleries of Interest:</h3>

<h4>W3C Schools: simple CSS Galleries</h4>
    
<p><a href="https://www.w3schools.com/cSS/css_image_gallery.asp" target="_blank" title="W3C schools simple CSS Galleries">CSS Image Gallery</a></p>

<p>W3C Schools has some simple html and css only gallery code examples for a very basic, no-frills presentation. The code samples are using divs only, so it might be a bit out-dated semantically, but the basics are there.</p>

    
<p><a href="https://www.w3schools.com/howto/howto_css_image_grid_responsive.asp" target="_blank" title="CSS Responsive Image Grid">CSS Responsive Image Grid</a></p>
<p>This one is a responsive masonry grid presentation, using flexbox, showing the code on how one can step-by-step create a basic gallery.</p> 

<h4>Filtronio</h4>
<p><a href="https://codecanyon.net/item/filtronio-css3-portfolio/6588280?ref=1stwebdesigner" target="_blank" title="Filtronio css gallery">Filtronio CSS Gallery</a></p>
<p>This is a nice gallery option, again using just HMTL and CSS.  This one does cost $5, but for such a low price you get a number of features: Fully responsive, animated overlays, a number of layouts, category filters, and integration into Wordpress and Joomla sites.</p>

<h4>Gridder</h4>
<p><a href="https://github.com/oriongunning/gridder" target="_blank" title="Gridder Github Repo">Gridder: jquery Gallery Github Repo</a><br>
<a href="http://oriongunning.github.io/gridder/" target="_blank" title="Gridder Demo page">Gridder: jquery Gallery Github Demo page</a></p>
<p>Gridder is a jquery plugin that displays a thumbnail grid that functions very similar to a Google images search results page. Responsive, it will change from one to 4 columns depending on screen width, and clicking a thumbnail image brings up a larger image and text description section to the side.</p>  
<p>An additional intriguing feature is the option to add external plug-ins in the expanded content section, which could have a number of great options one could use for additional images, shopping cart options, etc.</p>
