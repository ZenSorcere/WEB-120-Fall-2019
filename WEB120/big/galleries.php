<h4>Wowslider</h4>
<p><a href="http://wowslider.com/" target="_blank" title="Wowslider">Wowslider: Free jquery slider</a></p>
<p>Another site with a jquery slider, but this one is free, and comes with a no-coding required GUI option for quick and easy setup.  Full of fun and eye-catching animation transition options, this one looks like a great option for getting an attention grabbing gallery slider setup quickly.</p>

    
 
    
