 <p>A popular internet search question, “Which is better: Responsive Websites vs. Mobile Apps?” leads to quite a number of articles espousing one or the other. It’s not an easy black-or-white question to answer, as there are definite pros and cons for each. To dive into this a little bit, we should preface what we are specifically referring to.</p>


<h3>What are Responsive web design and Mobile apps?</h3>

<p>Responsive websites are able to adapt to various screen sizes continually and seamlessly, with content appearing/disappearing and rearranging to be optimal for whatever viewport size is required. Responsive mobile websites tend to have smaller fonts, less white space, and less elements on the page than their desktop versions do.</p>

<p>Mobile apps, on the other hand, are designed specifically around an OS (AppleOS and Android being the two most are familiar with), with a unique code base for each.</p> 

<h3>Pros and Cons of Responsive Design</h3>

<p>The two most commonly cited positives for Responsive mobile websites are time and cost.  With one design approach, your website can be viewed across most modern browsers.  This saves time in not having to recreate your code from the bottom up for each OS you encounter, and you can complete changes across viewports quickly.</p>


<p>There are definitely some drawbacks, however. A Responsive mobile site requires your customers to always have an internet connection. If a mobile user has no internet connection for various potential reasons, you have no connection with the customer whatsoever.</p>

<p>You also have to be careful how the site is designed, as what looks and performs great on a desktop computer with a dedicated internet connection may be slow to load on a mobile viewport; if your responsive design doesn’t take into account how to handle any large picture files, animations, third party elements, user data tracking software, etc, your website’s user experience could be severely hampered as page loading slows to a crawl, and may drain the mobile device’s battery.</p>

<p>Additionally, personalized content isn’t an available option as the user data from previous interactions isn’t saved, and requires the user to enter their credentials each time. There are no push notifications available on a responsive mobile site, either.  Also, mobile features like GPS and the camera aren’t accessible.</p>

<h3>Pros and Cons of Mobile Apps</h3>

<p>Mobile apps are usually designed to be efficient, and as such tend to faster in functionality than accessing a website. They are designed with only mobile devices in mind, so load faster as they don’t have as much information to process as the user experience is optimized.</p>

<p>The overall app functionality broader, in that it can take advantage of device features the responsive site could not. In addition to the aforementioned aspects of camera and gps, an app can easily integrate with other apps and services on the device, thereby enhancing the user experience.</p>

<p>Apps also have advantages in brand awareness and retention.  There is an icon on the device desktop the user sees everyday, the app can send personalized push notifications to grab a customer’s attention, and can save user data on the device, thus allowing for continued ease of use, easier shopping cart conversion, and alleviate privacy concerns.</p>

<p>The biggest cons for mobile apps, like the benefits of responsive designed sites on mobile, are time and cost. Designing mobile apps requires a different skillset than designing responsively, in addition to designing unique sets of code for each OS the app is to be made for. In addition, things will take longer with the various approval processes for each OS system (Apple’s Appstore can take a number of days to have something approved and appear for purchase).</p>

<h3>Which to Use?</h3>

<p>With the growing trend on mobile usage for internet use, apps are not something to ignore. 90% of all mobile use was via apps (Barnaart 2019) last year, showing how much mobile apps are in customer’s lives.  However, other research shows that “most consumers prefer to shop and search more broadly, hence the persistent strength of the mobile web browser as a driver of shopping experiences.” (Kaplan 2017).  With mobile devices generating 52% of all worldwide web traffic ("Mobile share of website visits worldwide 2018" 2019), it’s clear all websites should be including responsive design to engage with users on mobile. If your site doesn’t require interactions with customers to be engaging and customized, the cost and time expenditure for a good app may not be needed. However, if those elements are a priority, the mobile app should be given serious weight.
</p>
    
 
    
